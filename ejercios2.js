// Solicitar al usuario ingresar un número
var numeroDecimal = parseFloat(prompt("Por favor, ingresa un número:"));

// Solicitar al usuario ingresar un número entero
var numeroEntero = parseInt(prompt("Por favor, ingresa otro número:"));

// Calcular la suma de los dos números ingresados
var suma = numeroDecimal + numeroEntero;

// Mostrar el resultado en pantalla
alert("El resultado de la suma es " + suma);

document.write("La suma es " + suma);
