// Solicitar al usuario ingresar un número
var numeero = parseFloat(prompt("Por favor, aingresa un número:"));

// Restar el 15% al número ingresado
var resultado = numeero - numeero * 0.15;

// Mostrar el resultado en pantalla
alert("El resultado después de restar el 15% es: " + resultado);
document.write("Tú resultado es " + resultado);
